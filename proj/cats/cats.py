def accuracy(typed, reference):
    """Return the accuracy (percentage of words typed correctly) of TYPED
    when compared to the prefix of REFERENCE that was typed.

    Arguments:
        typed: a string that may contain typos
        reference: a string without errors

    >>> accuracy('Cute Dog!', 'Cute Dog.')
    50.0
    >>> accuracy('A Cute Dog!', 'Cute Dog.')
    0.0
    >>> accuracy('cute Dog.', 'Cute Dog.')
    50.0
    >>> accuracy('Cute Dog. I say!', 'Cute Dog.')
    50.0
    >>> accuracy('Cute', 'Cute Dog.')
    100.0
    >>> accuracy('', 'Cute Dog.')
    0.0
    >>> accuracy('', '')
    100.0
    """
    typed_words = split(typed)
    reference_words = split(reference)
    # BEGIN PROBLEM 3
    
    #if they are just identical
    if typed_words == reference_words:
        return 100.0

    def is_match(w1, w2):
        w1.replace('\t', '')
        w2.replace('\t', '')
        if w1 == w2:
            return True
        return False

    type_len = len(typed_words)
    if type_len == 0:
        return 0.0

    ref_len = len(reference_words)
    loop_len = min(type_len, ref_len)
    count = 0
    for i in range(loop_len):
        cur_type_word = typed_words[i]
        cur_ref_word = reference_words[i]
        if is_match(cur_type_word, cur_ref_word):
            count = count + 1
    return 100 * count / type_len
    # END PROBLEM 3


def wpm(typed, elapsed):
    """Return the words-per-minute (WPM) of the TYPED string.

    Arguments:
        typed: an entered string
        elapsed: an amount of time in seconds

    >>> wpm('hello friend hello buddy hello', 15)
    24.0
    >>> wpm('0123456789',60)
    2.0
    """
    assert elapsed > 0, 'Elapsed time must be positive'
    # BEGIN PROBLEM 4
    words_count = len(typed) / 5
    return 60 * words_count / elapsed
    # END PROBLEM 4


